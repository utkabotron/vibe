from utils.bot_utils import parse_time_input

# Тестирование различных форматов ввода времени
test_inputs = [
    '2:30',  # Формат часы:минуты
    '1.5',   # Десятичное число с точкой
    '1,5',   # Десятичное число с запятой
    '2.0',   # Целое число с десятичной точкой
    '2',     # Целое число
    '0:30',  # Только минуты
    '2:5',   # Минуты без ведущего нуля
    '2:60',  # Некорректное количество минут
    'abc',   # Некорректный ввод
    ''       # Пустая строка
]

print("Тестирование функции parse_time_input:")
for input_str in test_inputs:
    result = parse_time_input(input_str)
    print(f"'{input_str}' -> {result}")
